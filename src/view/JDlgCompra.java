      public Compra viewBean() {
        Compra compra = new Compra();
        int id = Integer.parseInt(jTxtIdCompra.getText());

        compra.setIdcompra(id);
       Fornecedor fornecedor = (Fornecedor) jCboFornecedor.getSelectedItem();
        compra.setId_fornecedor(fornecedor.getIdfornecedor());
        
         SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
        try {
            compra.setData_compra(formato.parse(jFmtData.getText()));
        } catch (ParseException ex) {
            Logger.getLogger(JDlgUsuarios.class.getName()).log(Level.SEVERE, null, ex);
        }
         compra.setValor_total_compra(Double.valueOf(jTxtTotal.getText()));
         compra.setForma_pagamento(jTxtFormaPagamento.getText()); 

         compra.setStatus_compra(jCboStatus.getSelectedIndex());
       
        
      
        
        return compra;
    }

    public void beanView(Compra compra) {
        String id = String.valueOf(compra.getIdcompra());
    
        jTxtIdCompra.setText(id);
        
      int id_fornecedor = compra.getId_fornecedor();
        Fornecedor_DAO fornecedor_DAO = new Fornecedor_DAO();
        jCboFornecedor.setSelectedItem(fornecedor_DAO.List(id_fornecedor));
        
      SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
       jFmtData.setText(formato.format(compra.getData_compra()));
         jTxtTotal.setText(String.valueOf(compra.getValor_total_compra()));
         jTxtFormaPagamento.setText(compra.getForma_pagamento());
         jCboStatus.setSelectedIndex(compra.getStatus_compra());
       
         
    }
    

